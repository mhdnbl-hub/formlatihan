<div class="mb-5">
  <h2 class="fw-bold mb-3">Pengertian Teknologi Informasi</h2>
  <p class="fs-5">
    Teknologi Informasi (TI) adalah bidang yang mencakup seluruh teknologi yang digunakan untuk mengelola, memproses, menyimpan, dan menyebarkan informasi. Ini meliputi hardware, software, jaringan komunikasi, dan basis data.
  </p>
</div>

<div class="mb-5">
  <h2 class="fw-bold mb-3">Manfaat Teknologi Informasi</h2>
  <ul class="fs-5">
    <li>Mempermudah akses informasi di mana saja dan kapan saja.</li>
    <li>Meningkatkan efisiensi kerja melalui proses yang lebih cepat dan otomatis.</li>
    <li>Mempermudah komunikasi jarak jauh secara real-time.</li>
    <li>Memacu inovasi di berbagai bidang industri.</li>
    <li>Mendukung pengambilan keputusan berbasis data.</li>
  </ul>
</div>

